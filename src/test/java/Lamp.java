public class Lamp {
    //--------------------ATTRIBUTES---------------------
    private String manufacturer;
    private String color;
    private double capacity;
    private String lightingType;
    private String lampBaseType;

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public double getCapacity() {
        return capacity;
    }

    public void setCapacity(double capacity) {
        this.capacity = capacity;
    }

    public String getLightingType() {
        return lightingType;
    }

    public void setLightingType(String lightingType) {
        this.lightingType = lightingType;
    }

    public String getLampBaseType() {
        return lampBaseType;
    }

    public void setLampBaseType(String lampBaseType) {
        this.lampBaseType = lampBaseType;
    }
}

